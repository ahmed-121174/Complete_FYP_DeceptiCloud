/**
 * Initialize icons in the dashboard
 * Replaces empty icon spans with SVG icons
 */
function initializeIcons() {
    // Navigation icons
    const navItems = document.querySelectorAll('.nav-item');
    navItems.forEach(item => {
        const page = item.dataset.page;
        const iconSpan = item.querySelector('.nav-icon');
        if (iconSpan && !iconSpan.innerHTML.trim()) {
            const iconKey = page || 'overview';
            if (ICONS[iconKey]) {
                iconSpan.innerHTML = ICONS[iconKey];
            }
        }
    });
    
    // Stat card icons
    const statCards = document.querySelectorAll('.stat-card');
    statCards.forEach(card => {
        const iconDiv = card.querySelector('.stat-icon');
        const label = card.querySelector('.stat-label')?.textContent.trim();
        
        if (iconDiv && !iconDiv.innerHTML.trim() && label) {
            let iconKey = null;
            if (label.includes('Attacks Detected')) iconKey = 'attacksDetected';
            else if (label.includes('Honeypot Events')) iconKey = 'honeypotEvents';
            else if (label.includes('System Health')) iconKey = 'systemHealth';
            else if (label.includes('Confidence')) iconKey = 'confidence';
            else if (label.includes('Threat Actors')) iconKey = 'threatActors';
            else if (label.includes('Total Honeypots')) iconKey = 'honeypotEvents';
            else if (label.includes('Online')) iconKey = 'systemHealth';
            else if (label.includes('Total Attacks')) iconKey = 'attacksDetected';
            else if (label.includes('Active Sessions')) iconKey = 'profiles';
            
            if (iconKey && ICONS[iconKey]) {
                iconDiv.innerHTML = ICONS[iconKey];
            }
        }
    });
    
    // Empty state icons
    const emptyIcons = document.querySelectorAll('.empty-icon');
    emptyIcons.forEach(icon => {
        if (!icon.innerHTML.trim()) {
            icon.innerHTML = ICONS.emptyData;
        }
    });
}

// Auto-initialize when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initializeIcons);
} else {
    initializeIcons();
}

// Export for manual initialization if needed
window.ICONS = ICONS;
window.initializeIcons = initializeIcons;
